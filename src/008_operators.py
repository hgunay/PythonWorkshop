# Arithmetic Operators
varNumber = 1+2*3/4.0
print(varNumber)

varMod = 11%3
print(varMod)

varSquared = 7**2
print(varSquared)

varCubed = 2**3
print(varCubed)

# Operators with Strings
helloWorld = "hello" +" "+ "world"
print(helloWorld)

lotsOfHellos = "hello "*10
print(lotsOfHellos)

# Operators with Lists
evenNumbers = [2,4,6,8]
oddNumbers = [1,3,5,7]
allNumbers = oddNumbers + evenNumbers
print(allNumbers)

print([1,2,3]*3)